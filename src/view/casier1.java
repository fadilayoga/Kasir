    private void BTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTambahActionPerformed
        // TODO add your handling code here:
        String Jenis = CBarang.getSelectedItem().toString();//1-PNS
        String [] pisah = Jenis.split("-");//("1","PNS")
        String kodeJenis = pisah[0];
        int jenisPegawai = Integer.parseInt(kodeJenis);
        String jumlah = spinner.getValue().toString();
        int jml2 = Integer.parseInt(jumlah);
        kasir objvoid = new kasir(jml2);
        objvoid.setKasir(jenisPegawai);
        if(THarga.getText().equals("") && jml2 < 1){
        JOptionPane.showMessageDialog(this, "Harap pilih barang", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);  
        }else if(jml2 < 1){            
        JOptionPane.showMessageDialog(this, "Mohon Tentukan Jumlah Barang", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }else if(THarga.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Mohon Pilih Barang Terlebih Dahulu", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);  
        }else{        
//        String satujenis = Integer.toString(objvoid.Jumlah());
//        String banyak = Integer.toString(objvoid.Banyak());
//        String harga = Integer.toString(objvoid.Harga());
        tbl.addRow(new Object [] {objvoid.Nama_barang(), objvoid.Harga(), objvoid.Banyak(), objvoid.printJumlah()});        
        data.add(objvoid.Jumlah());    
        int total = 0;    
        for(int i=0; i<data.size();i++){
        total += data.get(i);
        }
        String Total = Integer.toString(total);
        TTotal.setText(Total);
        TData.setModel(tbl);
        THarga.setText("");
        spinner.setValue(0);
        }        
    }//GEN-LAST:event_BTambahActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int kol = TData.getSelectedRow();
        if(TData.getRowCount()==0){
        JOptionPane.showMessageDialog(this, "Harus ada barang yang dibeli", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }else if(TData.isRowSelected(kol)){        
        tbl.removeRow(kol);
        data.remove(kol);
        int total = 0;
        for(int i=0; i<data.size(); i++){
            total += data.get(i);
        }
        String Total = Integer.toString(total);
        TTotal.setText(Total);
        }else{
        JOptionPane.showMessageDialog(this, "Pilih barang yang ingin dihapus", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

