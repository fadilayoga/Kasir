    private void payActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payActionPerformed
        // TODO add your handling code here:
        //tidak bisa parse karena sistem membaca kosong
        String uang = bayar.getText();
        String total = TTotal.getText();
        if(TTotal.getText().equals("0") && bayar.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Harus ada barang yang dibeli", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }else if(bayar.getText().equals("")){
        JOptionPane.showMessageDialog(this, "masukkan uang anda", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }else if(TTotal.getText().equals("0")){
        JOptionPane.showMessageDialog(this, "Beli dulu baru bayar", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        bayar.setText("");
        }else if(Integer.parseInt(uang) < Integer.parseInt(total)){
        JOptionPane.showMessageDialog(this, "Maaf uang anda kurang", 
        "Pesan Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        bayar.setText("");
        }else{
        int total2 = Integer.parseInt(total);
        int uang2 = Integer.parseInt(uang);
        kasir obj = new kasir();
        kembalian.setText(obj.printJumlah(obj.Jumlah(uang2, total2)));
        JOptionPane.showMessageDialog(this, "Terima Kasih");
        TTotal.setText("0");
        bayar.setText("");
        kembalian.setText("0");
        data.clear();
        tbl.getDataVector().removeAllElements();
        tbl.fireTableDataChanged();
        }        
    }//GEN-LAST:event_payActionPerformed

